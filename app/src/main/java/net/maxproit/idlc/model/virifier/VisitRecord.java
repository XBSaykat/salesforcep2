package net.maxproit.idlc.model.virifier;

public class VisitRecord {
    public String mitDate = "";
    public String follDate = "";
    public String visit = "";
    public String remark = "";

    public String getMitDate() {
        return mitDate;
    }

    public void setMitDate(String mitDate) {
        this.mitDate = mitDate;
    }

    public String getFollDate() {
        return follDate;
    }

    public void setFollDate(String follDate) {
        this.follDate = follDate;
    }

    public String getVisit() {
        return visit;
    }

    public void setVisit(String visit) {
        this.visit = visit;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
